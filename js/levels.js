function prepareLevel(num, lang) {
  const data = levelData[num - 1]
  solution = data.blackboard.answer[0]

  updateInterfaceLanguage(lang)

  updateMissionInfo(data[lang].mission)
  updateTaskInfo(data[lang].task)
  updateExampleInfo(data[lang].example)
  updateCode(data.common.code)
  updateTextArea(data.common.textarea)
  updateComment(data[lang].comment)

  updateBoard(data.blackboard.face, data.blackboard.goal)
}

//Функция которая меняет язык интерфейса на сайте
function updateInterfaceLanguage(langLvl) {
  if (langLvl == 'ukr') {
    detailsMissionInfo.querySelector('summary').innerHTML = 'Що я маю робити?'
    detailsTaskInfo.querySelector('summary').innerHTML = 'Як мені це зробити?'
    detailsExampleInfo.querySelector('summary').innerHTML = 'Приклад'
  } else {
    detailsMissionInfo.querySelector('summary').innerHTML = 'What i have to do?'
    detailsTaskInfo.querySelector('summary').innerHTML = 'How can I do it?'
    detailsExampleInfo.querySelector('summary').innerHTML = 'Example'
  }


}

